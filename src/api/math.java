package api;

public class math {
	public static void main(String[] args) {
		System.out.println("-5.2的绝对值是"+Math.abs(-5.2));
		System.out.println("-5.2的正弦值是"+Math.sin(-5.2));
		System.out.println("4的平方根是"+Math.sqrt(4));
		System.out.println("81的立方根是"+Math.cbrt(81));
		System.out.println("3的4次方是"+Math.pow(3,4));
		System.out.println("求大于参数的最小整数"+Math.ceil(3.4));
		System.out.println("求小于参数的最大整数"+Math.floor(3.4));
		System.out.println(""+Math.round(-3.6));
		System.out.println("求小于参数的最大整数"+(int)(-3.4));
	}
}
